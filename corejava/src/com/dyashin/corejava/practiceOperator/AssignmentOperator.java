package com.dyashin.corejava.practiceOperator;

public class AssignmentOperator 
{
	public static void main(String[] args) 
	{
		int a = 10;
        System.out.println("A value is " + a);
        
        a += 20; 
        System.out.println("A value is " + a);
        
        a -= 20; 
        System.out.println("A value is " + a);
        
        a *= 60; 
        System.out.println("A value is " + a);
        
        a /= 10; 
        System.out.println("A value is " + a);
        
        a %= 20; 
        System.out.println("A value is " + a);
	}

}

