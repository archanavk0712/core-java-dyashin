package com.dyashin.corejava.practiceOperator;

public class RelationalOperator 
{
	public static void main(String[] args) 
	{
		int x = 20;
        int y = 40;
        
        System.out.println(x<10);
        System.out.println(x<=10);
        System.out.println(x>y);
        System.out.println(x>=10);
        System.out.println(x==y);
        System.out.println(x!=10);
        
	}

}