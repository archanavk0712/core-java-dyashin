package com.dyashin.corejava.day2;

public class OperationsRelational 
{
	public static void main(String[] args) 
	{
		int x=1, y=2;
		System.out.println(x==y);
		System.out.println(x<y);
		System.out.println(x>y);
		System.out.println(x<=y);
		System.out.println(x>=y);
		System.out.println(x!=y);

	}

}
