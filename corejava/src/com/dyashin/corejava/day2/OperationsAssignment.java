package com.dyashin.corejava.day2;

public class OperationsAssignment 
{
	public static void main(String[] args) 
	{
		int x=5;
		System.out.println(x);
		System.out.println(x+=2);
		System.out.println(x-=2);
		System.out.println(x*=2);
		System.out.println(x/=2);
		System.out.println(x%=2);	
	}
}

