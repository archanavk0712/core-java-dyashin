package com.dyashin.practiceOperator;

public class ArithmeticOperator 
{
	public static void main(String[] args) 
	{
		int num1 = 30;
		int num2 = 25;
		System.out.println("Sum of two numbers : " + (num1 + num2));
	    System.out.println("Difference of two numbers : "+ (num1 - num2));
	    System.out.println("Multiplication of two numbers : "+(num1 * 10));
	    System.out.println("Quotient of two numbers : " + (num1 / num2));
	    System.out.println("Reminder of two numbers : " + (num1 % num2));
	}
}
